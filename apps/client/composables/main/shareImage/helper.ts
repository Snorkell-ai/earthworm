export const copyImage = (canvasEl: HTMLCanvasElement, fullFormat: string) => {
  canvasEl?.toBlob((blob) => {
    if (blob) {
      navigator.clipboard.write([new ClipboardItem({ [blob.type]: blob })]).then(() => {
        console.log("Image copied.");
        return true
      });
    }
  }, fullFormat);
};

